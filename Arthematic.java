public class Arthematic {
    public static void main(String[] args){
        int a=10;
        int b=20;
        System.out.println("the value of a is :"+a);
        System.out.println("the value of a is :"+b);
        System.out.println("the value of a is :"+(a+b));
        System.out.println("the value of a is :"+(a-b));
        System.out.println("the value of a is :"+(a*b));
        System.out.println("the value of a is :"+(a/b));
        System.out.println("the value of a is :"+(a%b));

    }

}
